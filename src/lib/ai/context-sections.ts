/**
 * 生成コンテキストの共有セクション定義。
 * クライアント（studio ページ）とサーバ（API/ジョブランナー）の双方から
 * import されるため、DB 等のサーバ専用依存を持たないこと。
 */

/**
 * ジョブ分析（JTBD インタビュー結果）のコンテキストセクション。
 *
 * ジョブ分析はユーザーと対話して合意した「確定要望」なので、生テキストの
 * 入力資料・参考資料より優先して参照させる（矛盾時の挙動を未定義にしない）。
 */
export function jtbdSection(analysisResult: string): string {
  return [
    "## ジョブ分析（JTBDインタビューでユーザーと合意した確定要望）",
    "※以下の内容が「入力資料」「参考資料」と矛盾する場合は、必ずこのジョブ分析を優先すること。",
    analysisResult,
  ].join("\n");
}
